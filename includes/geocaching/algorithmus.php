        <div class="col gap-1">
            <h4>Was ist ein Algorithmus?</h4>
            <p>
            Stell dir vor, du sortierst deine Spotify-Playlist nach Länge der Songs. Du
            hast dabei automatisch einen "Algorithmus" verwendet - eine Schritt-für-Schritt Anleitung, um ein
            Problem zu lösen. In der Informatik sind Algorithmen der Grundbaustein für alles, von Suchmaschinen
            bis zu KI.
            </p>
            <h4>Der Bubble Sort Algorithmus</h4>
            <p>
                Einer der klassischen Sortier-Algorithmen ist "Bubble Sort" - nicht der effizienteste, aber super um
                zu verstehen, wie Algorithmen funktionieren. Er arbeitet wie Luftblasen, die im Wasser aufsteigen:
            </p>
            <ul class="indented">
                <li>Größere Zahlen "blubbern" nach oben</li>
                <li>Kleine Zahlen "sinken" nach unten</li>
                <li>Dies passiert durch wiederholtes Vergleichen und Tauschen</li>
            </ul>
        </div>

        <div class="col gap-1">
            <h4 class="color-magenta">Rätsel</h4>
            <p>Sortiere diese durcheinandergewürfelte Zahlenreihe aufsteigend: <b>[7, 5, 3, 1, 9, 8, 6, 4, 2]</b></p>
            <h4>So gehst du vor:</h4>
            <ol class="indented">
                <li>Vergleiche immer zwei benachbarte Zahlen</li>
                <li>Ist die linke größer? Tausche sie!</li>
                <li>Zähle jeden Tausch (<b>T</b> = wie oft musste getauscht werden?)</li>
                <li>Die mittlere Zahl der sortierten Reihe ist dein zweiter Schlüssel <b>M</b></li>
                <li><b>M</b> und <b>T</b> in diese Formel einsetzen:</li>
            </ol>
            <div class="elevated-box">
                <h4 class="color-magenta">Koordinaten-Formel</h4>
                <ol class="indented">
                    <li><b>N = 51° 0(M - 4).(T x 23 - 5)'</b></li>
                    <li><b>E = 13° (T x 2 - 1).(M x 80)'</b></li>
                </ol>
            </div>
        </div>

        <div class="col gap-1">
