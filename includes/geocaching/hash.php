        <div class="col gap-1">
            <h4>WAS IST EIN HASH?</h4>
            <p>
                Ein Hash ist wie ein einzigartiger Fingerabdruck für Daten. Egal ob es sich um einen Text, 
                ein Passwort oder eine Datei handelt - ein Hash verwandelt diese Informationen mit einer 
                speziellen Formel in eine feste Zeichenfolge, die aussieht wie ein zufälliges Durcheinander 
                von Buchstaben und Zahlen. Das Spannende daran: Auch wenn der Originalinhalt riesig ist, bleibt 
                der Hash immer gleich lang.
            </p>
            <p>
                Stell es dir vor wie eine Maschine, die dein Lieblingsbuch nimmt und daraus einen kurzen Code erstellt. 
                Ändert sich auch nur ein Buchstabe im Originaltext, sieht der Hash komplett anders aus. Hashes werden oft 
                genutzt, um Daten sicher zu speichern, zum Beispiel Passwörter, oder um zu prüfen, ob Daten unverändert 
                geblieben sind. Aber Achtung: Ein Hash kann nicht zurück in die Originaldaten umgewandelt werden - das 
                macht ihn so sicher!
            </p>
